#Leia quatro valores numéricos A, B, C e D. A seguir, calcule e mostre a diferença do produto de A e B pelo produto de C e D.


A = int(input('\nDIGITE O VALOR A:'))
B = int(input('\nDIGITE O VALOR B:'))
C = int(input('\nDIGITE O VALOR C:'))
D = int(input('\nDIGITE O VALOR D:'))
dif = (A * B) - (C * D)
print('\nDIFERENCA = {}'.format(dif))
